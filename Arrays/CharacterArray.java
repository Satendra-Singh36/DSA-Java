package Arrays;
public class CharacterArray {
    public static void main(String[] args) {
        char[] arr = {'m', 'u', 'm', 'm', 'y'};
        for (int x : arr) {
            System.out.print(x + " ");
        }
        System.out.println();
        for (char ch : arr) {
            System.out.print((char) ch + " ");
        }
    }
}
 
